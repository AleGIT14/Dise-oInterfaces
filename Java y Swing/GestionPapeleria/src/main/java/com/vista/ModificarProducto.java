    private void btnModProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModProdActionPerformed
        // TODO add your handling code here:
        Tablapapeleria nuevoArt = new Tablapapeleria();
        //setID del buscado
        nuevoArt.setCodigo(textCodigo.getText());
        nuevoArt.setFamilia(textFamilia.getText());
        nuevoArt.setProducto(textProducto.getText());
        nuevoArt.setPrecio(Double.valueOf(textPrecio.getText()));
        JOptionPane.showMessageDialog(this, "Producto modificado correctamente", "ModificaciÃ³n completada", JOptionPane.INFORMATION_MESSAGE);
        try {
            this.jpa.edit(nuevoArt);
            limpiarFormulario();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error al intentar modificar el producto", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnModProdActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        try {
            ControlarBD ctrl = new ControlarBD();
        Tablapapeleria tp = ctrl.buscarPorCodigo(textCodigo.getText());
        activarCamposText(true);

        textFamilia.setText(tp.getFamilia());
        textPrecio.setText(String.valueOf(tp.getPrecio()));
        textProducto.setText(tp.getProducto());
        
        textCodigo.setEnabled(false);
        
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Producto no encontrado", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        


    }//GEN-LAST:event_btnBuscarActionPerformed


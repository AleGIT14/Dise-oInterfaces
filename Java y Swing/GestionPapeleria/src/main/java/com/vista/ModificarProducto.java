    private void activarCamposText(boolean b) {
        if (b == false) {
            textProducto.setEnabled(false);
            textPrecio.setEnabled(false);
            textFamilia.setEnabled(false);
        } else {
            textProducto.setEnabled(true);
            textPrecio.setEnabled(true);
            textFamilia.setEnabled(true);
        }
    }

    private void limpiarFormulario() {
        textCodigo.setText("");
        textProducto.setText("");
        textPrecio.setText("");
        textFamilia.setText("");
        activarCamposText(false);
        textCodigo.setEnabled(true);
    }


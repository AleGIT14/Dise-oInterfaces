    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        ControlarBD crtl = new ControlarBD();
        
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        model.setRowCount(0);
        
        List<Tablapapeleria> list = crtl.findAll();
        
        for (Tablapapeleria listArt : list) {
                Object[] art = {listArt.getCodigo(), listArt.getProducto(), listArt.getFamilia(), listArt.getPrecio()};
                model.addRow(art);
        }
    }//GEN-LAST:event_btnListarActionPerformed


    private void btnNuevoProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoProdActionPerformed
        // TODO add your handling code here:
        if (validarDatos()) {

            ControlarBD controlarbd = new ControlarBD();
            Tablapapeleria tablapapeleria = new Tablapapeleria();
            tablapapeleria.setCodigo(txtfCodigo.getText());
            tablapapeleria.setFamilia(txtfFamilia.getText());
            tablapapeleria.setPrecio(Double.parseDouble(txtfPrecio.getText()));
            tablapapeleria.setProducto(txtfProducto.getText());
            controlarbd.create(tablapapeleria);

            JOptionPane.showMessageDialog(this, "Producto nuevo creado correctamente", "Producto creado", JOptionPane.INFORMATION_MESSAGE);
            int respuestaConfirmacion = JOptionPane.showConfirmDialog(this, "¿Desea insertar otro producto?", "Salir", JOptionPane.YES_NO_OPTION);
            if (respuestaConfirmacion == 0) {
                limpiarFormulario();
            } else {
                salir();
            }

        } else {
            JOptionPane.showMessageDialog(this, msgError, "Error de validación", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnNuevoProdActionPerformed


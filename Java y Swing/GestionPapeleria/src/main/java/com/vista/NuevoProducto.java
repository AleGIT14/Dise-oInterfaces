    private void limpiarFormulario() {
        txtfCodigo.setText("");
        txtfFamilia.setText("");
        txtfPrecio.setText("");
        txtfProducto.setText("");
    }

    private boolean validarDatos() {
        boolean validado = true;
        if (txtfCodigo.getText() == null || txtfCodigo.getText().equals("")) {
            msgError = "El campo codigo no puede estar vacío";
            validado = false;
            txtfCodigo.requestFocus();
        } else {
            if (txtfFamilia.getText() == null || txtfFamilia.getText().equals("")) {
                msgError = "El campo familia no puede estar vacío";
                validado = false;
                txtfFamilia.requestFocus();
            } else {
                if (txtfPrecio.getText() == null || txtfPrecio.getText().equals("")) {
                    msgError = "El campo precio no puede estar vacío";
                    validado = false;
                    txtfPrecio.requestFocus();
                } else {
                    if (!isNumeric(txtfPrecio.getText())) {
                        msgError = "El campo precio no es un valor numérico";
                        validado = false;
                        txtfPrecio.requestFocus();
                    } else {
                        if (txtfProducto.getText() == null || txtfProducto.getText().equals("")) {
                            msgError = "El campo producto no puede estar vacío";
                            validado = false;
                            txtfProducto.requestFocus();
                        } else {
                            crearLista();
                            for (int i = 0; i < listaPapeleria.size(); i++) {
                                if (listaPapeleria.get(i).getCodigo().equals(txtfCodigo.getText())) {
                                    msgError = "Ya existe un producto con ese código.";
                                    validado = false;
                                    txtfCodigo.requestFocus();
                                }
                            }
                        }
                    }
                }
            }
        }
        return validado;
    }

    private void salir() {
        MenuInicio ventana = new MenuInicio();
        ventana.setVisible(true);
        this.dispose();
    }

    private static boolean isNumeric(String cadena) {
        try {
            Double.parseDouble(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }


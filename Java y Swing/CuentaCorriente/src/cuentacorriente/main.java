    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        String t = fieldTitular.getText();
        String d = fieldDni.getText();
        String c = fieldCuenta.getText();

        if (!t.equals("")) {
            if (!d.equals("")) {
                if (comprobarDNI(d)) {
                    anadirCuenta(t, d, c);
                } else {
                    int yn = JOptionPane.showConfirmDialog(null, "El DNI ya existe, ¿Quieres añadirlo igualmente?", "¿ESTAS SEGURO?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (JOptionPane.YES_OPTION == yn) {
                        anadirCuenta(t, d, c);
                    } else {
                        JOptionPane.showMessageDialog(null, "Cliente no añadido", "¡INFO!", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Falta el campo DNI", "¡ATENCIÓN!", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falta el campo Titular", "¡ATENCIÓN!", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnInsertarActionPerformed


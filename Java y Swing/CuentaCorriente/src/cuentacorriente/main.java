    private boolean comprobarDNI(String DNI) {
        for (Cuenta c : listaCuentas) {
            if (c.getDni().equals(DNI)) {
                return false;
            }
        }
        return true;
    }

    private void anadirCuenta(String t, String d ,String c) {
        if (!c.equals("")) {
            Cuenta a = new Cuenta(t, d, Double.parseDouble(c));
            listaCuentas.add(a);
            JOptionPane.showMessageDialog(null, "Cliente añadido ✌", "¡INFO!", JOptionPane.INFORMATION_MESSAGE);

        } else {
            Cuenta b = new Cuenta(t, d);
            listaCuentas.add(b);
            JOptionPane.showMessageDialog(null, "Cliente añadido ✌", "¡INFO!", JOptionPane.INFORMATION_MESSAGE);
        }
    }


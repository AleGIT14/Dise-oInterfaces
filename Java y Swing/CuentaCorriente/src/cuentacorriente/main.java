    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        if (!listaCuentas.isEmpty()) {

            DefaultTableModel model = (DefaultTableModel) tableListar.getModel();

            model.setRowCount(0);

            for (Cuenta c : listaCuentas) {

                Object[] a = {c.getTitular(), c.getDni(), c.getCantidad()};

                model.addRow(a);

            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay nada que listar", "¡INFORMACIÓN!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnListarActionPerformed


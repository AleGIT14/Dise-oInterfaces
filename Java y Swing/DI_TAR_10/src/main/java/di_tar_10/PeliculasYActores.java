    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String a = txtBuscar.getText();
        ListModel<String> listModelActores = lstActores.getModel();

        for (int i = 0; i < numActores; i++) {
            if (listModelActores.getElementAt(i).equalsIgnoreCase(a)) {
                jTabbedPane1.setSelectedIndex(1);
                lstActores.setSelectedIndex(i);
                txtActores.setText(arrActores.get(i));
                txtInfo.setText("Mostrando actores");
                break;
            } else if (jTabbedPane1.getTitleAt(0).equalsIgnoreCase(a)) {
                jTabbedPane1.setSelectedIndex(0);
                txtInfo.setText("Mostrando sinopsis");
                break;
            } else if (jTabbedPane1.getTitleAt(1).equalsIgnoreCase(a)) {
                jTabbedPane1.setSelectedIndex(1);
                txtInfo.setText("Mostrando actores");
                break;
            } else {
                txtInfo.setText("Parametro no encontrado");
            }
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

    private void lstActoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstActoresMouseClicked
        for (int i = 0; i < numActores; i++) {
            if (lstActores.getSelectedValue().equals(arrNomAct.get(i))) {
                txtActores.setText(arrActores.get(i));
            }
        }
    }//GEN-LAST:event_lstActoresMouseClicked


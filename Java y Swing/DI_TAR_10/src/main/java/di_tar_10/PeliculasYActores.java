    private void rellenarDatos(BufferedReader bfr) {
        try {

            DefaultListModel model = new DefaultListModel();

            txtSip.setText(bfr.readLine());

            numActores = Integer.parseInt(bfr.readLine());

            for (int i = 0; i < numActores; i++) {

                //nombre actores
                String nomActor = bfr.readLine();
                model.addElement(nomActor);
                arrNomAct.add(nomActor);

                //descripcion actores
                arrActores.add(bfr.readLine());
            }
            lstActores.setModel(model);

        } catch (IOException e) {
            System.out.println("Error lectura");
        }
    }

